package com.registration.modal;

public class Response {

	private String memberId;

	public Response() {
		super();
	}

	public Response(String memberId) {
		super();
		this.memberId = memberId;
	}

	public String getMemberId() {
		return memberId;
	}

	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}

}
